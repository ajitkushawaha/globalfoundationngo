"use client";
import { ParallaxScroll } from "@/components/ui/parallax-scroll";
import { useEffect, useState } from "react";

interface GalleryImage {
  _id: string;
  title: string;
  description?: string;
  cloudinaryUrl: string;
  width: number;
  height: number;
  format: string;
  category: string;
  tags: string[];
  sortOrder: number;
}

export default function ParallaxScrollDemo() {
  const [images, setImages] = useState<string[]>([]);
  const [loading, setLoading] = useState(true);

  useEffect(() => {
    const fetchImages = async () => {
      try {
        const response = await fetch('/api/gallery/category/parallax?limit=20');
        if (response.ok) {
          const data = await response.json();
          const imageUrls = data.data.map((img: GalleryImage) => img.cloudinaryUrl);
          setImages(imageUrls);
        } else {
          // Fallback to default images if API fails
          setImages(defaultImages);
        }
      } catch (error) {
        console.error('Error fetching parallax images:', error);
        setImages(defaultImages);
      } finally {
        setLoading(false);
      }
    };

    fetchImages();
  }, []);

  if (loading) {
    return (
      <div className="flex items-center justify-center h-64">
        <div className="animate-spin rounded-full h-8 w-8 border-b-2 border-primary"></div>
      </div>
    );
  }

  return <ParallaxScroll images={images} />;
}

